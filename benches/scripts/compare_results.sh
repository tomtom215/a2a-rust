# Compare benchmark results across A2A SDK languages.
#
# Usage:
#   ./benches/scripts/compare_results.sh
#
# This script reads result files from benches/results/ and produces a
# Markdown comparison table. Each language SDK should deposit its results
# in the following format:
#
#   benches/results/<language>-<timestamp>.json
#
# The JSON file should contain:
#   {
#     "language": "python",
#     "sdk": "a2a-sdk-python",
#     "workloads": {
#       "echo_roundtrip": { "median_us": 1234, "p95_us": 1500, "p99_us": 1800 },
#       "stream_events":  { "median_us": 2345, "p95_us": 2800, "p99_us": 3200 },
#       "serialize_agent_card": { "median_us": 45, "p95_us": 52, "p99_us": 60 },
#       "concurrent_50":  { "median_us": 5000, "p95_us": 6000, "p99_us": 7000 },
#       "minimal_overhead": { "median_us": 800, "p95_us": 950, "p99_us": 1100 }
#     }
#   }
#
# For Rust results, the script extracts median values from criterion's
# JSON output in target/criterion/.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
RESULTS_DIR="$SCRIPT_DIR/../results"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
OUTPUT_FILE="$RESULTS_DIR/comparison.md"

# ── Header ───────────────────────────────────────────────────────────────────

cat > "$OUTPUT_FILE" <<'HEADER'
# A2A SDK Cross-Language Benchmark Comparison

> Auto-generated by `benches/scripts/compare_results.sh`.
> All numbers are **median latency in microseconds** unless noted otherwise.

## Methodology

- All SDKs hit the **same Rust echo server** to isolate client-side overhead.
- Workloads use **identical JSON payloads** (canonical fixtures).
- Each measurement includes **warm-up iterations** before timing.
- Results use **median ± MAD** to resist outlier pollution.

## Workloads

| Workload | Description |
|----------|-------------|
| `echo_roundtrip` | Send 256-byte text, receive echo response (full HTTP round-trip) |
| `stream_events` | Stream 3 events (Working + Artifact + Completed) |
| `serialize_agent_card` | Serialize/deserialize reference AgentCard |
| `concurrent_50` | 50 concurrent send-message requests |
| `minimal_overhead` | Noop executor — pure SDK overhead |

## Results

HEADER

# ── Collect per-language results ─────────────────────────────────────────────

echo "Scanning $RESULTS_DIR for result files..."

LANGUAGES=()
declare -A LANG_FILES

for f in "$RESULTS_DIR"/*.json; do
    [ -f "$f" ] || continue
    lang=$(basename "$f" | cut -d'-' -f1)
    LANGUAGES+=("$lang")
    LANG_FILES[$lang]="$f"
done

if [ ${#LANGUAGES[@]} -eq 0 ]; then
    echo "No result files found in $RESULTS_DIR/"
    echo "Run benchmarks first: ./benches/scripts/run_benchmarks.sh"
    exit 1
fi

# Remove duplicates
LANGUAGES=($(printf '%s\n' "${LANGUAGES[@]}" | sort -u))

echo "Found results for: ${LANGUAGES[*]}"

# ── Build comparison table ───────────────────────────────────────────────────

# Table header
{
    printf "| Workload |"
    for lang in "${LANGUAGES[@]}"; do
        printf " %s |" "$lang"
    done
    printf "\n"

    printf "|----------|"
    for _ in "${LANGUAGES[@]}"; do
        printf "--------|"
    done
    printf "\n"
} >> "$OUTPUT_FILE"

WORKLOADS=(
    echo_roundtrip
    stream_events
    serialize_agent_card
    concurrent_50
    minimal_overhead
)

for workload in "${WORKLOADS[@]}"; do
    printf "| \`%s\` |" "$workload" >> "$OUTPUT_FILE"
    for lang in "${LANGUAGES[@]}"; do
        if [ "$lang" = "rust" ]; then
            # Try to extract from criterion estimates
            ESTIMATE_FILE="$REPO_ROOT/target/criterion/cross_language/${workload}/rust/new/estimates.json"
            if [ -f "$ESTIMATE_FILE" ]; then
                # Extract median point estimate in nanoseconds, convert to microseconds
                median_ns=$(python3 -c "
import json, sys
with open('$ESTIMATE_FILE') as f:
    d = json.load(f)
print(int(d['median']['point_estimate']))
" 2>/dev/null || echo "0")
                if [ "$median_ns" != "0" ]; then
                    median_us=$((median_ns / 1000))
                    printf " %s us |" "$median_us" >> "$OUTPUT_FILE"
                else
                    printf " (run benchmarks) |" >> "$OUTPUT_FILE"
                fi
            else
                printf " (run benchmarks) |" >> "$OUTPUT_FILE"
            fi
        else
            # Extract from language result JSON
            result_file="${LANG_FILES[$lang]}"
            median=$(python3 -c "
import json, sys
with open('$result_file') as f:
    d = json.load(f)
w = d.get('workloads', {}).get('$workload', {})
print(w.get('median_us', 'N/A'))
" 2>/dev/null || echo "N/A")
            printf " %s us |" "$median" >> "$OUTPUT_FILE"
        fi
    done
    printf "\n" >> "$OUTPUT_FILE"
done

# ── Footer ───────────────────────────────────────────────────────────────────

cat >> "$OUTPUT_FILE" <<FOOTER

## How to reproduce

\`\`\`bash
# Rust
./benches/scripts/run_benchmarks.sh --bench cross_language

# Python (requires a2a-sdk-python)
./benches/scripts/cross_language_python.sh

# Go (requires a2a-sdk-go)
./benches/scripts/cross_language_go.sh

# JavaScript (requires a2a-sdk-js)
./benches/scripts/cross_language_js.sh

# Generate comparison table
./benches/scripts/compare_results.sh
\`\`\`

## Notes

- Lower is better for all latency measurements.
- Rust results come from criterion (statistical benchmarking with warm-up).
- Other SDK results should use equivalent warm-up methodology.
- All tests use loopback HTTP (127.0.0.1) to eliminate network variance.
- The purpose is **not competition** but rather to provide hard, verified
  numbers for deployment planning at scale (Anthropic, Google, etc.).
FOOTER

echo ""
echo "Comparison written to: $OUTPUT_FILE"
cat "$OUTPUT_FILE"
